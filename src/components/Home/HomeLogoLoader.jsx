/**
 * Logo fill animation (GSAP mask) — home page only, placed above the banner.
 */
export default function HomeLogoLoader() {
  const reactId = useId().replace(/:/g, "");
  const clipPathId = `home-logo-fill-mask-${reactId}`;
  const maskRectRef = useRef(null);
  const [isVisible, setIsVisible] = useState(true);

  useEffect(() => {
    const rect = maskRectRef.current;
    if (!rect) return;

    if (window.matchMedia?.("(prefers-reduced-motion: reduce)")?.matches) {
      rect.setAttribute("y", "0");
      rect.setAttribute("height", "198");
      setIsVisible(false);
      return;
    }

    const ctx = gsap.context(() => {
      gsap.fromTo(
        rect,
        { attr: { y: 198, height: 0 } },
        {
          attr: { y: 0, height: 198 },
          duration: 1,
          ease: "power1.inOut",
          onComplete: () => setIsVisible(false),
        },
      );
    });

    return () => ctx.revert();
  }, []);

  if (!isVisible) return null;

  return (
    <section
      className="gs__home-logo-loader"
      // data-theme="section-dark"
      aria-label="Guided Solutions"
    >
      <div className="gs__home-logo-loader-inner">
        <svg
          width="184"
          height="198"
          viewBox="0 0 184 198"
          fill="none"
          xmlns="http://www.w3.org/2000/svg"
          aria-hidden
        >
          <defs>
            <clipPath id={clipPathId}>
              <rect ref={maskRectRef} x="0" y="198" width="184" height="0" />
            </clipPath>
          </defs>

          <g id="base-logo">
            <path d={PATH_A} fill="#333333" />
            <path d={PATH_B} fill="#333333" />
          </g>

          <g clipPath={`url(#${clipPathId})`}>
            <path d={PATH_A} fill="#FFFFFF" />
            <path d={PATH_B} fill="#FFFFFF" />
          </g>
        </svg>
      </div>
    </section>
  );
}
